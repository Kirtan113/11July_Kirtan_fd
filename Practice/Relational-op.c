//Relational operator
#include<stdio.h>
int main()
{
    int x,y;
    printf("Enter values of x and y:");
    scanf("%d%d",&x,&y);
 
    printf("x>y:%d\n",x>y);

    printf("x>=y:%d\n",x>=y);

    printf("x<y:%d\n",x<y);

    printf("x<=y:%d\n",x<=y);

    printf("x==y:%d\n",x==y);

    printf("x!=y:%d\n",x!=y); 
}